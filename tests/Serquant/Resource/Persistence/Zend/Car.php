class Car
{
    private $id;

    private $owner;

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }

    public function getOwner()
    {
        return $this->owner;
    }

    public function setOwner(Person $owner)
    {
        $this->owner = $owner;
    }
}